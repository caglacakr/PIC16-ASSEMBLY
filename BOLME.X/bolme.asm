	list		p=16f877A	; WHICH PIC WE USE
	#include	<p16f877A.inc>	; INCLUDING SFR REGISTER-S
	
	ORG 0X00   ;SELECT ORIGIN FOR RUNNING

	DEG1 EQU  0X21
	DEG2 EQU  0X22
	SONUC EQU 0X23
 
	MOVLW D'10'
	MOVWF DEG1
	
	MOVLW D'4'
	MOVWF DEG2
	
	BOLME
	    BSF STATUS,C
	    SUBWF DEG1,F
	    BTFSS STATUS,C
	    GOTO SONLAN 
	    INCF SONUC,F
	    GOTO BOLME
	
	SONLAN
	    END
	
	

