    __CONFIG H'3F31'
    list p=16f877a
    #include<p16f877a.inc>
    
    ORG 0X00
    
    KARASIMSEK EQU 0X20
    DEG1 EQU 0X21
    DEG2 EQU 0X22
 
    MOVLW 0XFF
    MOVWF DEG1
    
    MOVLW 0XFF
    MOVWF DEG2
 
    MOVLW 0X001
    MOVWF KARASIMSEK
    BANKSEL TRISB
    CLRF TRISB
    
    BANKSEL PORTB
    CLRF PORTB
    
    SOLAKAYDIR
	CALL GECIKME
	RLF KARASIMSEK,F
	MOVFW KARASIMSEK
	MOVWF PORTB
	BTFSS KARASIMSEK,7
	GOTO SOLAKAYDIR
	GOTO SAGAKAYDIR

    SAGAKAYDIR
	CALL GECIKME
	RRF KARASIMSEK,F
	MOVFW KARASIMSEK
	MOVWF PORTB
	BTFSS KARASIMSEK,0
	GOTO SAGAKAYDIR
	GOTO SOLAKAYDIR
    
    GECIKME
	DECFSZ DEG1,F
	GOTO DEG2AZALT
	MOVLW 0XFF
	MOVWF DEG1
	MOVLW 0XFF
	MOVWF DEG2
	RETURN
	
    DEG2AZALT
	DECFSZ DEG2,F
	GOTO DEG2AZALT
	GOTO GECIKME

	END

