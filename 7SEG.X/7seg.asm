    list p = 16f877a
    #include<p16f877a.inc>
    
    ORG 0X00
    
    BASLANGIC
        DEG1    EQU 0X23
	DEG2    EQU 0X24
	MOVLW 0XFF
	MOVWF DEG1
    
	MOVLW 0XFF
	MOVWF DEG2

	CLRF	  PORTB		   
	BANKSEL TRISB		    
	CLRF	  TRISB
	MOVLW	0x06		    
	MOVWF	ADCON1		    
	MOVLW	b'00111100'	 
	MOVWF	TRISA
	BCF	STATUS,RP0
	CLRF	PORTB

    DD
	MOVLW 0X01
	MOVWF PORTA
	CLRW
	MOVLW 0X00
	CALL  LOOK_UP
	MOVWF PORTB
	CALL  GECIKME
	
	MOVLW 0X02
	MOVWF PORTA
	CLRW
	MOVLW 0X06
	CALL  LOOK_UP
	MOVWF PORTB
	CALL  GECIKME
	
	GOTO DD
	
	
    LOOK_UP
	ADDWF	PCL,F		    ;W REGTEKI DEGERI PROGRAM COUNTER PCL INE EKLE VE UYGUN DISPLAY CIKTISINI W YA AL
	RETLW	0x3F
	RETLW	0x06
	RETLW	0x5B
	RETLW	0x4F
	RETLW	0x66
	RETLW	0x6D
	RETLW	0x7D
	RETLW	0x07
	RETLW	0x7F
	RETLW	0x6F
	
    GECIKME
	DECFSZ DEG1,F
	GOTO DEG2AZALT
	MOVLW 0X0A
	MOVWF DEG1
	MOVLW 0X03
	MOVWF DEG2
	RETURN
	
    DEG2AZALT
	DECFSZ DEG2,F
	GOTO DEG2AZALT
	GOTO GECIKME


END
